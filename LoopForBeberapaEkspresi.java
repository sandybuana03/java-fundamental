public class LoopForBeberapaEkspresi {
    public static void main (String[] args) {
        // Eksekusi dilakukan selama penjumlahan i+j <= 200
        for (int i = 1, j = 1; (i + j) <= 200 ; i++, j++) { // Terdapat beberapa ekspresi
            System.out.println(i + " Sandy");
            System.out.println(j + " Buana");
            System.out.println("----------");
        } // Tutup for
    }
}
