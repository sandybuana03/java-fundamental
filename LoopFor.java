public class LoopFor {
    public static void main (String[] args) {
        // Eksekusi dilakukan 100x selama i <= 100
        for (int i = 1; i <= 100 ; i++) // i = 1 adalah aksi awal
            System.out.println(i + " Sandy Buana"); // Tidak perlu kurung kurawal
    }                                               // Jika statemen hanya ada satu
}
